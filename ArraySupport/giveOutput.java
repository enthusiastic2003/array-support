 
package ArraySupport;
public class giveOutput
{
    public int outputInt(int a[])
    {
	for(int i=0;i<=a.length-1;i++)
	    {
		System.out.println(a[i]);
	    }
	return 0;
	
    }
    public int outputDouble(double a[])
    {
	for(int i=0;i<=a.length-1;i++)
	    {
		System.out.println(a[i]);
	    }
	return 0;
	
    }
    public int outputString(String a[])
    {
	for(int i=0;i<=a.length-1;i++)
	    {
		System.out.println(a[i]);
	    }
	return 0;
    }
}